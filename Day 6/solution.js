//// Task 1:Write a Function to Convert Celsius to Fahrenheit

function celsiusToFahrenheit(celsius) {
  let fahrenheit = (celsius * 9) / 5 + 32;
  return fahrenheit;
}

let tempInFahrenheit = celsiusToFahrenheit;

console.log(tempInFahrenheit(45));

